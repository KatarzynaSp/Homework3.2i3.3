public class Math {

    public boolean isEven(int a){
        boolean b = a%2==0;
        return b;
    }
    public boolean isOdd(int a){
        boolean b = a%2==1;
        return b;
    }
    public double circleField(double a){
        double P = 3.14* a*a;
        return P;
    }
    public int power(int a){
        return a*a;
    }
}
